class Aritmetika184220028: 
    @staticmethod
    def tambah(a, b):
        return a + b 
    @staticmethod
    def kurang(a, b):
        return a - b 
    @staticmethod
    def kali(a, b):
        return a * b 
    @staticmethod
    def bagi(a, b):
        return a / b 
    @staticmethod
    def bagi_int(a, b):
        return a // b 
    @staticmethod
    def sisabagi(a, b):
        return a % b 
    @staticmethod
    def pangkat(a, b):
        return a ** b
    
print(Aritmetika184220028.tambah(10, 3))
print(Aritmetika184220028.kurang(10, 3))
print(Aritmetika184220028.kali(10, 3))
print(Aritmetika184220028.bagi_int(10, 3))
print(Aritmetika184220028.sisabagi(10, 3))
print(Aritmetika184220028.pangkat(10, 3))
obj = Aritmetika184220028()
print(obj.tambah(10, 3))
